# range in for loop

for _ in range(0,10,2): # initial, final, step value
    print("Hello vmro")
    print(_)
    
for _ in range(10,-1,-1): # to reverse the range . Ranges can be negative. 10 to -1 is used to print 0
    print(_)
    
for _ in range(3):
    print(list(range(1,11)))
    
for _ in range(10,-11,-1):  # prints -10 to 10 in reverse order
    print(_)