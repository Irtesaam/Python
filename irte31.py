# Modules are like different python files and using import, we can access it
# Package is like collection of modules and it is a folder containing python files or modules
# To import from package, use : import shopping.shopping_cart
# Everytime we create a package, we must have empty __init__.py file to tell interpreter that it is package
# Pycharm is useful in organizing large projects as it contains packages modules etc
# To import from large number of packages, use : from <packgename>.<packgename> import <modulename>
# To import everything from a package : from utility import *